var num = [5,2,9,3]
num.push(4) // Coloca um valor no ARRAY na ultima posição
num.sort() // Organiza o ARRAY em ordem Numerica crescente e Alfabetica
num.indexOf(2) // procura o valor dentro do ARRAY e retorna a chave onde ele se encontra (obs: se não existir retorna (-1) que é false)
num.push(1)
console.log(num)
console.log(`O vetor tem ${num.length} posições`) // .length mostra a quantidade de posições dentro do array e pode ser usado para variaveis simples.
console.log(`O primeiro valor do vetor é ${num[0]}`)

