//ATRIBUIÇÃO (=)
var a = 20
// O sinal de (=) atribui valor a um valor como String ou Number.


//INCREMENTO E DECREMENTO 
//Incremento
var c = 30
c++
// 30 + 1 = 31
console.log(c) // Resultado => 31

//Decremento
var j = 30
j--
// 30 - 1 = 29
console.log(j) // Resultado => 29


//AUTO - ATRIBUIÇÃO 
var n = 3 
//Auto atribuição de soma
n += 4
// expressão igual a (n = n + 4)
console.log(`O valor de (n) agora será => ${n}`) // Valor de (n) é igual a => 7 

//Auto atribuição de multiplicação
n *= 4 
console.log(`O valor de (n) agora será => ${n}`)

//Auto atribuição de divisão
n /= 4
console.log(`O valor de (n) agora será => ${n}`)

//Auto atribuição de raiz ao quadrado
n **= 2
console.log(`O valor de (n) agora será => ${n}`)

//Auto atribuição de resto
n %= 5
console.log(`O valor de (n) agora será => ${n}`)


