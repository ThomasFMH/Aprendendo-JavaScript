//Conceito global podendo variar e ser usado de modo geral.
var nameVar = "Guilherme"

// Conceito de bloco de código, podendo ser utilizado dentro e fora.
let nameLet = "Guilherme"

// Conceito de valor constante, não sendo alterado ao longo do código.
const nameConst = "Guilherme"

console.log(`nameVar: ${nameVar}`)
console.log(`nameLet: ${nameLet}`)
console.log(`nameConst: ${nameConst}`)

